#!/bin/bash

# N0DE Environment Management System
# Consolidates and manages all environment configuration

# Load error handling library
source "/home/sol/n0de-deploy/lib/error-handler.sh"
init_error_handler "$(basename "$0")"

# Configuration
ENV_DIR="/home/sol/n0de-deploy"
TEMPLATE_FILE="$ENV_DIR/.env.production.template"
PRODUCTION_FILE="$ENV_DIR/.env.production"
DATABASE_FILE="$ENV_DIR/.env.database"
REDIS_FILE="$ENV_DIR/.env.redis"
MERGED_FILE="$ENV_DIR/.env.merged"
BACKUP_DIR="$ENV_DIR/env-backups"

# Environment validation rules
declare -A REQUIRED_VARS=(
    ["NODE_ENV"]="production"
    ["PORT"]="3001"
    ["DATABASE_URL"]="postgresql://"
    ["REDIS_URL"]="redis://"
    ["JWT_SECRET"]="minimum_32_chars"
)

declare -A OPTIONAL_VARS=(
    ["GITHUB_CLIENT_ID"]="OAuth GitHub integration"
    ["GOOGLE_CLIENT_ID"]="OAuth Google integration"  
    ["STRIPE_SECRET_KEY"]="Payment processing"
    ["RESEND_API_KEY"]="Email notifications"
    ["FRONTEND_URL"]="Frontend application URL"
    ["BACKEND_URL"]="Backend API URL"
)

# Create environment manager structure
init_env_manager() {
    log_info "Initializing environment manager"
    
    require_directory "$BACKUP_DIR" true "Environment backups directory"
    
    # Ensure template exists
    if [ ! -f "$TEMPLATE_FILE" ]; then
        create_production_template
    fi
    
    log_success "Environment manager initialized"
}

